#include <stdio.h>

int main() {
    // Create variables
    int myNum = 15;         // Integer
    float myFloat = 6.9;    // Floating point number
    char myChar = 'S';      //Character

    // Print variables
    printf("%d\n", myNum);
    printf("%f\n", myFloat);
    printf("%c\n", myChar);

    //End main()
    return 0;
}